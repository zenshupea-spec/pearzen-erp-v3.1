const GRID_MIN_YEAR = 2020;

function gridMaxYear(): number {
  return new Date().getFullYear();
}

function chronoMonthKeys(): string[] {
  return buildChronoMonthKeys(GRID_MIN_YEAR, gridMaxYear() + 1);
}

const BANK_FEE_TOLERANCE = 300;

function monthKeyBefore(monthKey: string): string | undefined {
  return monthKeyBeforeInChrono(monthKey, chronoMonthKeys());
}

function monthKeyAfter(monthKey: string): string | undefined {
  return monthKeyAfterInChrono(monthKey, chronoMonthKeys());
}

function rolloverSourceLabel(monthKey: string): string {
  const mo = parseInt(monthKey.split('-')[1] ?? '0', 10);
  const short = MONTH_SHORT[mo - 1];
  if (!short) return monthKey;
  return `${short} ${monthKey.slice(0, 4)}`;
}

function sumCreditNotes(cell: InvoiceCell): number {
  return (cell.creditNotes ?? []).reduce((s, cn) => s + cn.amount, 0);
}

/** Net shortfall on a PARTIAL invoice after receipts and credit notes (rollover-eligible). */
function partialNetShortfall(cell: InvoiceCell): number {
  if (cell.status !== 'PARTIAL' || cell.amountReceived == null) return 0;
  const raw = cell.totalAmount - cell.amountReceived - sumCreditNotes(cell);
  return raw <= BANK_FEE_TOLERANCE ? 0 : Math.max(0, raw);
}

/** Rollover shown on a month: derived from prior PARTIAL when linked, else stored value. */
function effectiveRolloverDebt(
  cell: InvoiceCell,
  priorMonthCell: InvoiceCell | undefined,
): number {
  if (cell.rolloverFromMonth && priorMonthCell?.status === 'PARTIAL') {
    return partialNetShortfall(priorMonthCell);
  }
  return cell.rolloverDebt ?? 0;
}

function applyNextMonthRollover(
  invoices: Partial<Record<string, InvoiceCell>>,
  partialMonthKey: string,
  partialCell: InvoiceCell,
): Partial<Record<string, InvoiceCell>> {
  const nextKey = monthKeyAfter(partialMonthKey);
  if (!nextKey) return invoices;

  const shortfall = partialNetShortfall(partialCell);
  const nextExisting = invoices[nextKey];
  if (!nextExisting || nextExisting.status === 'NONE') return invoices;

  if (shortfall > 0) {
    return {
      ...invoices,
      [nextKey]: {
        ...nextExisting,
        rolloverDebt: shortfall,
        rolloverFromMonth: rolloverSourceLabel(partialMonthKey),
      },
    };
  }

  if (nextExisting.rolloverDebt != null || nextExisting.rolloverFromMonth) {
    const { rolloverDebt: _rd, rolloverFromMonth: _rf, ...rest } = nextExisting;
    return { ...invoices, [nextKey]: rest };
  }
  return invoices;
}

// ─── Helpers ──────────────────────────────────────────────────────────────────

function lkr(n: number) {
  const sign = n < 0 ? '-' : '';
  const abs = Math.abs(n);
  if (abs >= 1_000_000) return `${sign}LKR ${(abs / 1_000_000).toFixed(2)}M`;
  if (abs >= 1_000)     return `${sign}LKR ${(abs / 1_000).toFixed(1)}K`;
  return `${sign}LKR ${abs.toLocaleString()}`;
}

const ROLLING_5Y_MONTH_COUNT = 60;

/** Last 60 months ending at the current billing month (rolling 5 years) */
function buildRolling5YearMonthKeys(endMonthKey: string, fallbackMonthKeys: string[]): string[] {
  const chrono = chronoMonthKeys();
  const endIdx = chrono.indexOf(endMonthKey);
  if (endIdx < 0) return fallbackMonthKeys;
  const startIdx = Math.max(0, endIdx - (ROLLING_5Y_MONTH_COUNT - 1));
  return chrono.slice(startIdx, endIdx + 1);
}

/** Client balance still owed after credit notes and any partial receipts */
function invoiceOutstandingBalance(
  cell: InvoiceCell,
  priorMonthCell: InvoiceCell | undefined,
): number {
  if (cell.status === 'NONE' || cell.status === 'PAID') return 0;
  const rollover = effectiveRolloverDebt(cell, priorMonthCell);
  const netDue = cell.totalAmount + rollover - sumCreditNotes(cell);
  if (cell.status === 'PARTIAL' || cell.status === 'SETTLED_FINED') {
    return Math.max(0, netDue - (cell.amountReceived ?? 0));
  }
  return Math.max(0, netDue);
}

function sumOutstandingForMonthKeys(
  clients: ClientRecord[],
  monthKeys: string[],
): number {
  const keySet = new Set(monthKeys);
  let total = 0;
  for (const c of clients) {
    for (const [monthKey, cell] of Object.entries(c.invoices)) {
      if (!cell || !keySet.has(monthKey)) continue;
      const priorKey = monthKeyBefore(monthKey);
      const priorCell = priorKey ? c.invoices[priorKey] : undefined;
      total += invoiceOutstandingBalance(cell, priorCell);
    }
  }
  return total;
}

type KpiFilter = 'rolling5y' | 'thisMonth' | 'unpaid' | 'partial';

interface KpiInvoiceRow {
  clientId: string;
  clientName: string;
  monthKey: string;
  monthLabel: string;
  invoiceNo: string;
  status: PayStatus;
  amount: number;
  cell: InvoiceCell;
  rolloverSourceCell?: InvoiceCell;
}

function compareKpiRows(a: KpiInvoiceRow, b: KpiInvoiceRow): number {
  const monthCmp = b.monthKey.localeCompare(a.monthKey);
  if (monthCmp !== 0) return monthCmp;
  return a.clientName.localeCompare(b.clientName);
}

function collectOutstandingRows(
  clients: ClientRecord[],
  monthKeys: string[],
  variant: ArInvoicingVariant,
): KpiInvoiceRow[] {
  const keySet = new Set(monthKeys);
  const rows: KpiInvoiceRow[] = [];
  for (const c of clients) {
    for (const [monthKey, cell] of Object.entries(c.invoices)) {
      if (!cell || !keySet.has(monthKey)) continue;
      const priorKey = monthKeyBefore(monthKey);
      const priorCell = priorKey ? c.invoices[priorKey] : undefined;
      const amount = invoiceOutstandingBalance(cell, priorCell);
      if (amount <= 0) continue;
      rows.push({
        clientId: c.clientId,
        clientName: c.clientName,
        monthKey,
        monthLabel: monthKeyToLabel(monthKey),
        invoiceNo: cell.invoiceNo,
        status: displayPayStatus(cell.status, variant),
        amount,
        cell,
        rolloverSourceCell: priorCell,
      });
    }
  }
  return rows.sort(compareKpiRows);
}

function collectUnpaidRows(
  clients: ClientRecord[],
  monthKeys: string[],
  variant: ArInvoicingVariant,
): KpiInvoiceRow[] {
  const keySet = new Set(monthKeys);
  const rows: KpiInvoiceRow[] = [];
  for (const c of clients) {
    for (const [monthKey, cell] of Object.entries(c.invoices)) {
      if (!cell || !keySet.has(monthKey)) continue;
      const isUnpaid =
        cell.status === 'PENDING' ||
        cell.status === 'PENDING_MD_VERIFICATION' ||
        (hideDisputeUi(variant) && cell.status === 'DISPUTED');
      if (!isUnpaid) continue;
      const priorKey = monthKeyBefore(monthKey);
      const priorCell = priorKey ? c.invoices[priorKey] : undefined;
      rows.push({
        clientId: c.clientId,
        clientName: c.clientName,
        monthKey,
        monthLabel: monthKeyToLabel(monthKey),
        invoiceNo: cell.invoiceNo,
        status: displayPayStatus(cell.status, variant),
        amount: invoiceOutstandingBalance(cell, priorCell),
        cell,
        rolloverSourceCell: priorCell,
      });
    }
  }
  return rows.sort(compareKpiRows);
}

function collectPartialRows(
  clients: ClientRecord[],
  monthKeys: string[],
  variant: ArInvoicingVariant,
): KpiInvoiceRow[] {
  const keySet = new Set(monthKeys);
  const rows: KpiInvoiceRow[] = [];
  for (const c of clients) {
    for (const [monthKey, cell] of Object.entries(c.invoices)) {
      if (!cell || !keySet.has(monthKey) || cell.status !== 'PARTIAL') continue;
      const priorKey = monthKeyBefore(monthKey);
      const priorCell = priorKey ? c.invoices[priorKey] : undefined;
      rows.push({
        clientId: c.clientId,
        clientName: c.clientName,
        monthKey,
        monthLabel: monthKeyToLabel(monthKey),
        invoiceNo: cell.invoiceNo,
        status: cell.status,
        amount: invoiceOutstandingBalance(cell, priorCell),
        cell,
        rolloverSourceCell: priorCell,
      });
    }
  }
  return rows.sort(compareKpiRows);
}

