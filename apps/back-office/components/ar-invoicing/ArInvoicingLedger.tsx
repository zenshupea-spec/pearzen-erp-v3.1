// ─── Traffic-Light Cell ───────────────────────────────────────────────────────

function TrafficCell({
  cell,
  onClick,
  nextHasRollover,
  hideDisputes = false,
}: {
  cell: InvoiceCell | undefined;
  onClick: () => void;
  nextHasRollover?: boolean;
  hideDisputes?: boolean;
}) {
  if (!cell || cell.status === 'NONE') {
    return (
      <td className="w-[72px] min-w-[72px] px-1 py-3 text-center">
        <span className="inline-flex h-6 w-6 items-center justify-center">
          <Minus className="h-3 w-3 text-slate-300" />
        </span>
      </td>
    );
  }

  const displayStatus = hideDisputes && cell.status === 'DISPUTED' ? 'PENDING' : cell.status;
  const st = STATUS_CELL[displayStatus];

  const label =
    displayStatus === 'PAID'                    ? 'Paid'       :
    displayStatus === 'PENDING'                 ? 'Unpaid'     :
    displayStatus === 'PARTIAL'                 ? 'Partial'    :
    displayStatus === 'PENDING_MD_VERIFICATION' ? 'Verifying'  :
    displayStatus === 'DISPUTED'                ? 'Disputed'   :
    displayStatus === 'SETTLED_FINED'           ? 'Settled†'   : '—';

  const showChain = cell.status === 'PARTIAL' && nextHasRollover;

  return (
    <td className="w-[72px] min-w-[72px] px-1 py-3 text-center">
      <button
        type="button"
        onClick={onClick}
        title={`${cell.invoiceNo} · ${lkr(cell.totalAmount)}`}
        className={`group mx-auto inline-flex items-center justify-center gap-1 whitespace-nowrap rounded-full border px-2 py-0.5 text-[10px] font-bold transition-all hover:scale-110 hover:shadow-md ${st.pill}`}
      >
        {st.dot && <span className={`h-1.5 w-1.5 flex-shrink-0 rounded-full ${st.dot}`} />}
        {label}
        <ChevronRight className="h-2.5 w-2.5 -ml-0.5 opacity-0 transition-opacity group-hover:opacity-100" />
      </button>
      {showChain && (
        <div
          className="mt-0.5 flex justify-center"
          title="Shortfall carried forward to next month"
        >
          <Link2 className="h-2.5 w-2.5 text-slate-300" />
        </div>
      )}
    </td>
  );
}

