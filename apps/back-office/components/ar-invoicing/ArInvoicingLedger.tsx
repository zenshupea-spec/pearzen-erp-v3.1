function isProofImageUrl(url: string): boolean {
  if (url.startsWith('data:image/')) return true;
  if (url.startsWith('blob:')) return true;
  if (/^https?:\/\//i.test(url)) {
    if (/\.(jpe?g|png|gif|webp|bmp|svg|avif|heic|heif)(\?|#|$)/i.test(url)) return true;
    if (url.includes('/storage/v1/object/')) return true;
  }
  return false;
}

function isProofPdfUrl(url: string): boolean {
  if (url.startsWith('data:application/pdf')) return true;
  return /^https?:\/\//i.test(url) && /\.pdf(\?|#|$)/i.test(url);
}

