/**
 * Regenerate packages/supabase/database.generated.ts from remote Supabase schema.
 * Run: npm run types:supabase
 *
 * Requires NEXT_PUBLIC_SUPABASE_URL and SUPABASE_ACCESS_TOKEN (or linked `supabase login`).
 */

import { execSync } from 'node:child_process';
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const outPath = join(root, 'packages/supabase/database.generated.ts');

function loadEnv() {
  for (const file of ['apps/back-office/.env.local', '.env']) {
    try {
      const text = readFileSync(join(root, file), 'utf8');
      for (const line of text.split('\n')) {
        const m = line.match(/^([^#=\s][^=]*)=(.*)$/);
        if (m) process.env[m[1].trim()] = m[2].trim().replace(/^['"]|['"]$/g, '');
      }
    } catch {
      /* next */
    }
  }
}

loadEnv();

const supabaseUrl = process.env.NEXT_PUBLIC_SUPABASE_URL || '';
const projectRef = supabaseUrl.match(/https:\/\/([^.]+)\.supabase\.co/)?.[1];

if (!projectRef) {
  console.error('❌ NEXT_PUBLIC_SUPABASE_URL must be set to a *.supabase.co project URL');
  process.exit(1);
}

if (!process.env.SUPABASE_ACCESS_TOKEN?.trim()) {
  console.error('❌ SUPABASE_ACCESS_TOKEN is required for remote type generation.');
  console.error('   Or run: npx supabase login && npx supabase link --project-ref', projectRef);
  process.exit(1);
}

const types = execSync(
  `npx supabase gen types typescript --project-id ${projectRef}`,
  { cwd: root, encoding: 'utf8', env: process.env },
);

const header =
  '/** Generated by Supabase MCP — do not edit manually. Regenerate: npm run types:supabase */\n\n';

writeFileSync(outPath, header + types.trim() + '\n');
console.log(`✅ Wrote ${outPath}`);
