/**
 * Copy Supabase (and related) env vars into each app's .env.local so Next.js can reach the backend.
 *
 * Sources (first file wins per key): .env.seed.tmp → .env.local (repo root) → .env
 *
 * Run: npm run wire:backend
 */

import { existsSync, readFileSync, writeFileSync, mkdirSync } from 'fs';
import { dirname, join } from 'path';
import { fileURLToPath } from 'url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');

const SOURCE_FILES = ['.env.seed.tmp', '.env.local', '.env'];

const SHARED_KEYS = [
  'NEXT_PUBLIC_SUPABASE_URL',
  'NEXT_PUBLIC_SUPABASE_ANON_KEY',
  'SUPABASE_SERVICE_ROLE_KEY',
];

const BACK_OFFICE_KEYS = [
  ...SHARED_KEYS,
  'ENCRYPTION_KEY',
  'NEXT_PUBLIC_FIELD_PWA_URL',
  'NEXT_PUBLIC_SM_PWA_URL',
  'NEXT_PUBLIC_BACK_OFFICE_PORT',
  'NEXT_PUBLIC_TENANT_BASE_DOMAIN',
  'NEXT_PUBLIC_DEV_TENANT_SLUG',
  'NEXT_PUBLIC_DEV_TENANT_SUBDOMAINS',
  'NEXT_PUBLIC_TENANT_SUBDOMAINS_LIVE',
  'NEXT_PUBLIC_FORGE_HOST',
  'NEXT_PUBLIC_PLATFORM_HOSTS',
  'HEAD_OFFICE_LAT',
  'HEAD_OFFICE_LNG',
  'HEAD_OFFICE_RADIUS_KM',
  'NEXT_PUBLIC_GOOGLE_MAPS_API_KEY',
  'GOOGLE_MAPS_API_KEY',
];

const FIELD_PWA_KEYS = [
  ...SHARED_KEYS,
  'FIELD_PWA_AUTH_EMAIL_TEMPLATE',
  'FIELD_PWA_AUTH_PASSWORD_TEMPLATE',
  'FIELD_PWA_AUTH_PASSWORD',
];

const SM_PWA_KEYS = [...SHARED_KEYS];

const CLIENT_PWA_KEYS = [
  ...SHARED_KEYS,
  'NEXT_PUBLIC_CUSTOMER_MENU_COMPANY_ID',
];

/** Default café company for local customer menu (see .env.example). */
const DEFAULT_CUSTOMER_MENU_COMPANY_ID = '29fbb2ff-6aa6-46c4-8b2d-d19eebb2874e';

/** Dev-only 32-char key when ENCRYPTION_KEY is missing (HR NIC/phone encrypt). */
const DEV_ENCRYPTION_KEY = 'pearzen-local-dev-encryption-32';

function parseEnvFile(content) {
  const out = {};
  for (const line of content.split('\n')) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const m = trimmed.match(/^([^=]+)=(.*)$/);
    if (!m) continue;
    const key = m[1].trim();
    let value = m[2].trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    out[key] = value;
  }
  return out;
}

function loadMergedEnv() {
  const merged = {};
  for (const file of SOURCE_FILES) {
    const path = join(root, file);
    if (!existsSync(path)) continue;
    Object.assign(merged, parseEnvFile(readFileSync(path, 'utf8')));
    console.log(`  read ${file}`);
  }
  return merged;
}

function pick(env, keys) {
  const lines = ['# Generated by npm run wire:backend — do not commit secrets'];
  const missing = [];
  for (const key of keys) {
    let value = env[key];
    if (key === 'ENCRYPTION_KEY' && !value) {
      value = DEV_ENCRYPTION_KEY;
      console.warn(
        '  ENCRYPTION_KEY not in source files — using dev placeholder (set a real 32-char key for production).',
      );
    }
    if (key === 'NEXT_PUBLIC_FIELD_PWA_URL' && !value) {
      value = 'http://127.0.0.1:3001';
    }
    if (key === 'NEXT_PUBLIC_SM_PWA_URL' && !value) {
      value = 'http://127.0.0.1:3003';
    }
    if (key === 'FIELD_PWA_AUTH_EMAIL_TEMPLATE' && !value) {
      value = '{{epfNo}}@pearzen.local';
    }
    if (key === 'FIELD_PWA_AUTH_PASSWORD_TEMPLATE' && !value) {
      value = '{{epfNo}}';
    }
    if (key === 'NEXT_PUBLIC_CUSTOMER_MENU_COMPANY_ID' && !value) {
      value = DEFAULT_CUSTOMER_MENU_COMPANY_ID;
      console.warn(
        '  NEXT_PUBLIC_CUSTOMER_MENU_COMPANY_ID not in source files — using default café company UUID.',
      );
    }
    if (!value && SHARED_KEYS.includes(key)) {
      missing.push(key);
      continue;
    }
    if (value !== undefined && value !== '') {
      lines.push(`${key}=${value}`);
    }
  }
  return { content: `${lines.join('\n')}\n`, missing };
}

function writeAppEnv(appDir, keys, env) {
  const { content, missing } = pick(env, keys);
  const appPath = join(root, 'apps', appDir);
  mkdirSync(appPath, { recursive: true });
  const dest = join(appPath, '.env.local');
  writeFileSync(dest, content, 'utf8');
  console.log(`✓ ${dest}`);
  return missing;
}

console.log('Wiring Supabase env into apps…\n');

const env = loadMergedEnv();

if (!env.NEXT_PUBLIC_SUPABASE_URL || !env.NEXT_PUBLIC_SUPABASE_ANON_KEY) {
  console.error(
    '\nMissing NEXT_PUBLIC_SUPABASE_URL or NEXT_PUBLIC_SUPABASE_ANON_KEY.\n' +
      'Add them to .env.seed.tmp or repo-root .env.local, then run again.\n' +
      'Copy from Supabase → Project Settings → API.\n',
  );
  process.exit(1);
}

const allMissing = new Set();
for (const missing of writeAppEnv('back-office', BACK_OFFICE_KEYS, env)) {
  missing.forEach((k) => allMissing.add(k));
}
for (const missing of writeAppEnv('field-pwa', FIELD_PWA_KEYS, env)) {
  missing.forEach((k) => allMissing.add(k));
}
for (const missing of writeAppEnv('sm-pwa', SM_PWA_KEYS, env)) {
  missing.forEach((k) => allMissing.add(k));
}
for (const missing of writeAppEnv('client-pwa', CLIENT_PWA_KEYS, env)) {
  missing.forEach((k) => allMissing.add(k));
}

if (allMissing.size) {
  console.warn('\nOptional keys not set:', [...allMissing].join(', '));
}

if (!env.SUPABASE_SERVICE_ROLE_KEY) {
  console.warn(
    '\nSUPABASE_SERVICE_ROLE_KEY missing — seeds, bulk import, and Field login API will not work until you add it.',
  );
}

console.log('\nDone. Next: npm run check:backend  then  npm run dev');
